    /**
     * Este metodo se encarga de agregar los datos a un archivo binario
     * @param evt 
     */
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        sv.leerDeArchivo();
        sv.setNombreAsegurado(txtNombre.getText());
        sv.setEdad(Integer.parseInt(txtEdad.getText()));
        sv.setTipoCovertura(ComboCovertura.getItemAt(ComboCovertura.getSelectedIndex()));
        sv.setMontoAsegurado(Double.parseDouble(txtMontoAsegurado.getText()));
        double pago = sv.calcularPagoMensual(); 
        sv.setMontoPagoMensual(pago);
        txtPagoMensual.setText(String.valueOf(sv.getMontoPagoMensual()));
        sv.listaSeguroV.add(sv);        
        sv.imprimirEnArchivo();       
                
    }//GEN-LAST:event_btnAgregarActionPerformed

    /**
     * Este metodo se encarga de leer los datos del archivo
     * @param evt 
     */
    private void btnLeerDeArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLeerDeArchivoActionPerformed
        sv.leerDeArchivo();        
        JOptionPane.showMessageDialog(null, "Nombre: " + sv.getNombreAsegurado() + "\nEdad: "
            + sv.getEdad() + "\nMonto asegurado: " + sv.getMontoAsegurado() +
                    "\nPago mensual: " + sv.getMontoPagoMensual());
    }//GEN-LAST:event_btnLeerDeArchivoActionPerformed

    /**
     * Este metodo se encarga de buscar un seguro ya existente
     * @param evt 
     */
    private void btnBuscarSeguroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarSeguroActionPerformed
        String nombre = txtBuscarSeguro.getText();
        int x = 1;
        sv.leerDeArchivo();  
        for(Object Elementos : sv.listaSeguroV) {
            sv = (SeguroDeVida) Elementos;
            if(nombre == null ? sv.getNombreAsegurado() == null : nombre.equals(sv.getNombreAsegurado())) { // operador terneario
                JOptionPane.showMessageDialog(null, "Nombre: " + sv.getNombreAsegurado() + "\nEdad: "
                 + sv.getEdad() + "\nMonto asegurado: " + sv.getMontoAsegurado() +
                    "\nPago mensual: " + sv.getMontoPagoMensual());
                x = 1;
                break;
            }
            else {
                x = 0;
            }
        }        
        if(x == 0) {
            JOptionPane.showMessageDialog(null, "No existe");
        }
  
    }//GEN-LAST:event_btnBuscarSeguroActionPerformed


